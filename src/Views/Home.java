    //Boton de registro de informacion PC
    private void tbnIngresarRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbnIngresarRegistroActionPerformed
                
        //Instancias        
        SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd 00:00:00");        
        Complementos comple = new Complementos();
        Entradas entra = new Entradas();
        Date sis = new Date();     
        SimpleDateFormat formatter_sis = new SimpleDateFormat("yyyy-MM-dd hh:mm:ss");
                
        //Variables
        String Marca = "";
        String Modelo = "";
        String Anio = "";
        String Status = "";
        String FechaRegistro = "";
        Date FechaGarantia;
        String FechaGarantiaString = "";
        String Especificaciones = "";
        String Condiciones = "";
        String VersionWindows = "";
        String VersionOffice = "";
        String DetallesVersiones = "";
        Boolean FlagMarca = false;
        Boolean FlagModelo = false;
        Boolean FlagFechaGarantia = false;
        Boolean FlagEntrada = false;   
        String FechaSistema = "";
        
        FechaSistema = formatter_sis.format(sis);

        //Registro de PC
        Marca  = txtMarcaRegistro.getText();
        Modelo = txtModeloRegistro.getText();
        Anio = (String) cmbAniosRegistro.getSelectedItem();
        Status = (String) cmbEstatusRegistro.getSelectedItem();   
        FechaRegistro = FechaSistema;      
        FechaGarantia = dtFechaGarantiaRegistro.getDate();
        Especificaciones = txtAreaEspecificacionesRegistro.getText();
        Condiciones = txtAreaCondicionesRegistro.getText();
        VersionWindows = (String) cmbVersionWindowsRegistro.getSelectedItem();
        VersionOffice = (String) cmbVersionOfficeRegistro.getSelectedItem();
        DetallesVersiones = txtAreaDettaleVersionesRegistro.getText();
            
        //Validacion de valores en registro de PC            
        FlagMarca = comple.StringEsNulo(Marca,"Marca");//Marca
        FlagModelo = comple.StringEsNulo(Modelo,"Modelo");//Modelo
        FlagFechaGarantia = comple.DateEsNulo(FechaGarantia,"Fecha Garantia");//FechaGarantia
        if(FlagFechaGarantia == true)
        {
            FechaGarantiaString = formatter.format(FechaGarantia);   
        }
            
        try 
        {
            //Ingreso de registro de PC en base de datos
            entra.InsertRegistroPC(Marca, Modelo, Anio, Status, FechaRegistro,
                        FechaGarantiaString, Especificaciones, Condiciones, VersionWindows,
                        VersionOffice,DetallesVersiones );
        } 
        catch (SQLException ex) 
        {
            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_tbnIngresarRegistroActionPerformed
    
